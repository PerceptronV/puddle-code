export const ELEMENTS = ['earth', 'air', 'fire', 'water', 'metal', 'wood'];

export function memorableName(): string {
  const adjective = ADJECTIVES[randomInt(ADJECTIVES.length)];
  const noun = NOUNS[randomInt(NOUNS.length)];
  const element = ELEMENTS[randomInt(ELEMENTS.length)];
  return `${adjective}-${noun}-${element}`;
}
