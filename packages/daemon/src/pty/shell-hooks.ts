export interface ShellSpawnConfig {
  args: string[];
  env: Record<string, string>;
}

export class ShellHooks {
  constructor(
    private readonly zshDir: string,
    private readonly bashRc: string,
  ) {}

  /**
   * Extra args/env that arm the capture hook for a session shell. Dispatch is
   * by binary name; anything unrecognised spawns plain (no capture).
   */
  spawnConfig(shellPath: string): ShellSpawnConfig {
    const shell = basename(shellPath);
    if (shell === 'zsh') {
      const orig = process.env.ZDOTDIR;
      return {
        args: [],
        // PUDDLE_ORIG_ZDOTDIR carries the user's own ZDOTDIR through the shim;
        // its absence is the "user had none" sentinel, so only set it when real.
        env: { ZDOTDIR: this.zshDir, ...(orig ? { PUDDLE_ORIG_ZDOTDIR: orig } : {}) },
      };
    }
    if (shell === 'bash') return { args: ['--rcfile', this.bashRc], env: {} };
    return { args: [], env: {} };
  }
}

/**
 * Write the hook scripts under ~/.puddle/shell-hooks (regenerated every boot,
 * so upgrades refresh them). Files are sourced, never executed: 0600 in 0700
 * dirs.
 */
export function installShellHooks(paths: PuddlePaths): ShellHooks {
  const zshDir = join(paths.shellHooksDir, 'zsh');
  const bashDir = join(paths.shellHooksDir, 'bash');
  mkdirSync(zshDir, { recursive: true, mode: 0o700 });
  mkdirSync(bashDir, { recursive: true, mode: 0o700 });
  const write = (file: string, content: string) => writeFileSync(file, content, { mode: 0o600 });
  write(join(zshDir, '.zshenv'), zshZshenv(zshDir));
  write(join(zshDir, '.zprofile'), zshZprofile(zshDir));
  write(join(zshDir, '.zshrc'), zshZshrc(zshDir));
  write(join(zshDir, '.zlogin'), zshZlogin(zshDir));
  write(join(zshDir, 'env-report.zsh'), zshEnvReport());
  const bashRc = join(bashDir, 'bashrc.bash');
  write(bashRc, bashRcfile());
  return new ShellHooks(zshDir, bashRc);
}
